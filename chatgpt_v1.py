def ip():
	# 定义空列表用于存储每次生成的IP段
    IP = []
    for i in range(4):
        ip = random.randint(1, 255)
        # 添加每次生成的ip段到列表里面
        IP.append(str(ip))
    # 拼接列表里面的IP段数值
    IP = '.'.join(IP)
    return IP
